// Helper functions
export const getPropertyById = (id: string): Property | undefined => {
  return properties.find(p => p.id === id);
};

export const getPropertyBySlug = (slug: string): Property | undefined => {
  return properties.find(p => p.slug === slug);
};

export const getFeaturedProperties = (): Property[] => {
  return properties.filter(p => p.featured);
};

export const getPropertiesByStatus = (status: Property['status']): Property[] => {
  return properties.filter(p => p.status === status);
};

export const getPropertiesByType = (type: Property['propertyType']): Property[] => {
  return properties.filter(p => p.propertyType === type);
};

export const getPropertiesByCity = (city: string): Property[] => {
  return properties.filter(p => p.city.toLowerCase() === city.toLowerCase());
};

export const getPropertiesByAgent = (agentId: string): Property[] => {
  return properties.filter(p => p.agentId === agentId);
};

export const searchProperties = (query: string): Property[] => {
  const lowercaseQuery = query.toLowerCase();
  return properties.filter(p => 
    p.title.toLowerCase().includes(lowercaseQuery) ||
    p.city.toLowerCase().includes(lowercaseQuery) ||
    p.state.toLowerCase().includes(lowercaseQuery) ||
    p.description.toLowerCase().includes(lowercaseQuery) ||
    p.address.toLowerCase().includes(lowercaseQuery)
  );
};
